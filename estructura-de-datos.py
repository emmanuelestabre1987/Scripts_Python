#---------------------------
# TIPO DE DATOS SECUENCIA / COLECCION
#---------------------------


# list(lista) [coleccion ordenada y mutable]
lista= [1,2,3,4]

# tuple (tupla) [coleccion ordenada pero inmutable]
tupla = (1,2,3,4)

# range(rango) [secuencia inmutable de numeros]
rango= range(0,10)

#---------------------------
# TIPO DE DATOS DE MAPEO (MAPPING TYPE)
#---------------------------

# dict(diccionario) [coleccion no ordenada de pares clave-valor]
diccionario= {

    "nombre": "Emmanuel",
    "edad": 39,
    "tecnologias": ["Python","Javascript"]
}

#---------------------------
# TIPO DE DATOS CONJUNTOS
#---------------------------

# set(conjunto) [coleccion no ordenada y mutable de elementos unicos]
conjunto= {1,2,3,4}

#---------------------------
# TIPO DE DATOS NULOS
#---------------------------
# Nonetype (nulo) [Ausencia de valor o no definicion]
nulo= None 

